SELECT c.[Id]
      ,c.[Number]
      ,c.[Name]
      ,c.[Email]
      ,c.[PhoneNumber]
      ,c.[Website]
	  ,a.[Street]
      ,a.[Number]
      ,a.[PostalCode]
      ,a.[City]
      ,c.[TenantId]
      ,c.[ExtraProperties]
      ,c.[ConcurrencyStamp]
      ,c.[CreationTime]
      ,c.[CreatorId]
      ,c.[LastModificationTime]
      ,c.[LastModifierId]
      ,c.[IsDeleted]
      ,c.[DeleterId]
      ,c.[DeletionTime]
  FROM [dbo].[InterclubClubs] c
  LEFT JOIN [dbo].[InterclubClubAddresses] a ON c.Id = a.ClubId
  ORDER BY c.[Name]
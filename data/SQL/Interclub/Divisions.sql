SELECT d.[Id]
       ,CONCAT(YEAR(s.[StartDate]), '-', YEAR(s.[EndDate])) AS [Season]
      --,d.[Id]
      --,d.[SortOrder]
      ,d.[Name] AS [Division]
      --,d.[FrameCount]
      --,d.[MinPlayerClass]
  FROM [dbo].[InterclubDivisions] d
  LEFT JOIN [dbo].[InterclubSeasons] s ON d.[SeasonId] = s.[Id]
  ORDER BY s.[StartDate], d.[SortOrder]